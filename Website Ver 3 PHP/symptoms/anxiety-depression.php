            <h4>What is anxiety?</h4>
            <p>Anxiety is a common problem in multiple sclerosis. Anxiety is managed by learning how to recognise and cope with anxious thoughts. Anxiety is a feeling of worry or unease. Everyone feels anxious at some point, though these feels are usually short-lived and don't affect day to day life. Anxiety becomes a problem when it starts to affect your health and your enjoyment of life.</p>
            <h4>What is depression?</h4>
            <p>Depression is a constant feeling of sadness or low mood. Everyone feels sad or down at times but clinical depression is different. It is constantly present and interferes with your everyday activities. Depression can be mild, moderate or severe, depending on how much it affects your life, and you can experience different levels of depression at different times.</p>
